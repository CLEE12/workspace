package ch02;
public class Var2 {
	public static void main(String[] agrs) {
		int a1 = 3; //int a2 = 2.14, 정수는 default가 int
		double d1 = 3.14; //실수는 default가 double
		float f1 = 2.75f;  //float은 f사용후에 대입
	
		System.out.println("a1 = "+ a1);
		System.out.println("d1 = "+ d1);
		System.out.println("f1 = "+ f1);
		
	}

}
