package ch02;

public class RelationOp {
	public static void main(String[] args) {
		int i1 = 7;
		double d1 = 3.14;
		boolean b1 = true;
		
		boolean b2 = i1 > 10;
		System.out.println("b2 = "+b2);
		
		boolean b3 = b1 == b2;
		System.out.println("b3 = "+b3);
		
		boolean b4 = i1 > d1;
		System.out.println("b4 = "+b4);
		
		boolean b5 = i1 != 7;
		System.out.println("b5 = "+b5);
		
		
		
		
	}

}
